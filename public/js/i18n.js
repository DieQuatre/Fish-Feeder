// ==================== i18n ENGINE ====================
let currentLang = localStorage.getItem('ff_lang') || 'en';

function t(key, vars = {}) {
  let text = translations[currentLang]?.[key] || translations['en']?.[key] || key;
  Object.keys(vars).forEach(k => {
    text = text.replace(`{${k}}`, vars[k]);
  });
  return text;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else {
      el.innerHTML = text;
    }
  });

  // Update html lang
  document.documentElement.lang = currentLang === 'tr' ? 'tr' : 'en';
}

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('ff_lang', lang);
  applyTranslations();
  updateLangBtnState();
}

function toggleLang() {
  switchLang(currentLang === 'en' ? 'tr' : 'en');
}

function updateLangBtnState() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = currentLang === 'en' ? '🇹🇷 TR' : '🇬🇧 EN';
  });
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  updateLangBtnState();
});
